// function accepting two parameters
func calculate(_ firstNumber: Int, _ secondNumber: Int) {
    // multiply
    print(firstNumber * secondNumber)
    // divide
    print(firstNumber / secondNumber)
    // add
    print(firstNumber + secondNumber)
    // subtract
    print(firstNumber - secondNumber)
}
// call the function
calculate(10, 5)
// 50
// 2
// 15
// 5
